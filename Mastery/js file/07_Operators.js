// ------------- Number ----------------
let age = 22;

// ------------- String ----------------
let str = "Ram is a good boy."

// ------------- Datatypes (Primitive Datatypes)  ----------------
console.log(typeof age);
console.log(typeof str);

// ------------- Convert number into String ----------------
let n1 = 123;
n1 = String(n1);
console.log(n1); 

// 2nd Type 
age = age + "";
console.log(typeof age);

// ------------- Convert String into number ----------------
let a = "11";
a = Number(a);
console.log(typeof a);

// 2nd Type 
let newStr = "11";
newStr = +newStr;
console.log(typeof newStr);

