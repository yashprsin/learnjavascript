// ----------- Rules of naming variables ---------------

/* You can't start with number.
for example - 1value - (invalid)
*/
var value1 = 10;
console.log(value1); 

/* you can use only underscore _ or dollar sysmbol 
for example - first_name , _firstname, first$name, $firstname etc.
*/

var first_name = "Yash"; // Snake case writing
var _firstname = "Ravi"; // Snake case writing
var $firstname = "Rajat"; 
var first$name = "Ram"; 
var firstName = "ishaan"; // Camel case writing
console.log(first$name);
console.log(firstName);
console.log(first_name);
console.log(_firstname);
console.log($firstname);

