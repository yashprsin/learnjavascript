// ------------- Boolean & Comparison Operators ----------------

let n1 = 2;
let n2 = 5;
let n3 = 5;
let n4 = "5";

console.log(n1<n2);
console.log(n1>n2);

// == vs ===
console.log(n2==n3); // == check only value
console.log(n4===n3); // === check value and data type 

// != vs !==
console.log(n4!=n3); // === check value and data type 
console.log(n4!==n3); // === check value and data type 
